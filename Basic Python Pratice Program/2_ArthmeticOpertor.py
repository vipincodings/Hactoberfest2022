num1 = input("Enter the first number") 
num2 = input("Enter the second number")
# Addition of two numbers 
sum = num1 + num2
print(sum)
# Subsraction of two numbers 
sub = num1 - num2 
print(sub)
# Multiplication of two numbers
mul = num1 * num2
print(mul)
# Division of two numbers
if num2 == 0:
  print("Not valid")
div = num1 / num2 
print(div)
# Remainder after the division
rem = num1 % num2 
print(rem)
